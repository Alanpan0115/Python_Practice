# 8. 判斷字串是否為回文
# 回文指的是正著讀、反著讀都一樣的字串，例如 "level"、"madam"。
def a8_function(a):
    ans1 = [] #正向
    ans2 = [] #反向
    for i in range(0, len(a)):
        ans1.append(a[i])
    for i in range(len(a)-1, -1, -1): #從最後一個(len(a)-1)到第0個(-1)，反向前進(-1)
        ans2.append(a[i])
    print(ans1)
    print(ans2)
    if ans1 == ans2:
        print(f'"{a}" 是回文')
    else:
        print(f'"{a}" 不是回文')
qus = 'level'
a8_function(qus)    
